    private void insertRow(IMessage o, JTable j){
        
        mModel = (DefaultTableModel) j.getModel();
        
        mModel.insertRow(mModel.getRowCount(), new Object[] { false, o.getAuthor().getName(), o.getTimestamp().toString(), o.getContent() });
        
        j.setModel(mModel);
    }
    
